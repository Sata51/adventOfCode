func (e entry) isValid1() bool {
	count := 0
	for _, r := range e.word {
		if r == e.char {
			count++
		}
	}
	if e.min <= count && count <= e.max {
		return true
	}

	return false
}

func (e entry) isValid2() bool {
	strRune := []rune(e.word)
	var (
		firstChar  rune
		secondChar rune
	)
	for i, r := range strRune {
		if i+1 == e.min {
			firstChar = r
		}
		if i+1 == e.max {
			secondChar = r
		}
	}
	fmt.Printf("in %s looking for:%s - 1(%d):\"%s\" 2(%d):\"%s\" -> ", e.word, string(e.char), e.min, string(firstChar), e.max, string(secondChar))
	if (firstChar == e.char && secondChar == e.char) || (firstChar != e.char && secondChar != e.char) {
		fmt.Printf("invalid\n")
		return false
	}
	fmt.Printf("valid\n")
	return true

}
